"""Novel-based long-text temporal QA generator.

Uses Chinese novels as long-context sources for generating temporal QA pairs.
Each novel is split into paragraphs/segments, which serve as memory writes
for the memory system. QA pairs are generated by LLM based on the content.

This addresses the data insufficiency issue in BiTempQA v1 (3.2 writes/scene)
by providing 100-500+ writes per "conversation" (novel chapter).
"""

from __future__ import annotations

import json
import logging
import re
import time
from dataclasses import dataclass, field
from pathlib import Path
from typing import Dict, List, Optional, Tuple

from openai import OpenAI

logger = logging.getLogger(__name__)


@dataclass
class NovelSegment:
    """A segment of a novel (paragraph or chapter section)."""

    segment_id: str  # e.g., "novel_001_seg_042"
    novel_title: str
    chapter: str  # chapter name or number
    text: str
    position: int  # sequential position in novel
    timestamp_hint: str = ""  # extracted time reference, if any


@dataclass
class NovelQA:
    """A QA pair generated from a novel."""

    qa_id: str
    novel_title: str
    question: str
    gold_answer: str
    choices: List[str] = field(default_factory=list)
    correct_choice_index: Optional[int] = None
    answer_type: str = "multi_choice"  # multi_choice or abstractive
    question_type: str = ""  # temporal, factual, causal, etc.
    source_segment_ids: List[str] = field(default_factory=list)
    difficulty: str = "level_2"  # level_1, level_2, level_3


class NovelLoader:
    """Load and segment Chinese novels for benchmark use."""

    # Default chapter split pattern
    CHAPTER_PATTERN = re.compile(r"^第[一二三四五六七八九十百零〇\d]+[章节回集卷]\s*.+", re.MULTILINE)

    def __init__(
        self,
        books_dir: str = "/home/linkco/exa/llm-usefulEeb/书籍",
        min_segment_chars: int = 50,
        max_segment_chars: int = 500,
    ):
        self.books_dir = Path(books_dir)
        self.min_segment_chars = min_segment_chars
        self.max_segment_chars = max_segment_chars
        self._novels: Dict[str, List[NovelSegment]] = {}

    def list_books(self) -> List[str]:
        """List available novel files."""
        books = []
        for f in sorted(self.books_dir.glob("*.txt")):
            books.append(f.name)
        return books

    def load_novel(self, filename: str) -> List[NovelSegment]:
        """Load a novel and split into segments."""
        if filename in self._novels:
            return self._novels[filename]

        path = self.books_dir / filename
        if not path.exists():
            raise FileNotFoundError(f"Book not found: {path}")

        with open(path, "rb") as f:
            raw = f.read()

        # Auto-detect encoding: try UTF-8 first, then GB18030
        for enc in ("utf-8", "gb18030"):
            try:
                text = raw.decode(enc)
                # Verify it contains Chinese characters
                if sum(1 for c in text[:500] if "\u4e00" <= c <= "\u9fff") > 10:
                    break
            except (UnicodeDecodeError, LookupError):
                continue
        else:
            text = raw.decode("gb18030", errors="replace")

        # Clean up
        text = re.sub(r"\r\n", "\n", text)
        text = re.sub(r"\n{3,}", "\n\n", text)

        # Split into chapters
        chapters = self._split_chapters(text, filename)

        # Split chapters into segments
        segments = []
        seg_idx = 0
        novel_stem = Path(filename).stem

        for chapter_name, chapter_text in chapters:
            if not chapter_text.strip():
                continue

            paragraphs = [p.strip() for p in chapter_text.split("\n") if p.strip()]

            # Group short paragraphs into segments
            current_segment = ""
            para_positions = []

            for i, para in enumerate(paragraphs):
                current_segment += para + "\n"
                para_positions.append((i, para))

                # Flush segment if it's long enough
                if len(current_segment) >= self.min_segment_chars:
                    # If too long, split at sentence boundary
                    if len(current_segment) > self.max_segment_chars:
                        sub_segments = self._split_long_text(current_segment)
                        for sub in sub_segments:
                            if len(sub.strip()) >= self.min_segment_chars:
                                seg_id = f"novel_{novel_stem}_seg_{seg_idx:04d}"
                                segments.append(NovelSegment(
                                    segment_id=seg_id,
                                    novel_title=novel_stem,
                                    chapter=chapter_name,
                                    text=sub.strip(),
                                    position=seg_idx,
                                ))
                                seg_idx += 1
                    else:
                        seg_id = f"novel_{novel_stem}_seg_{seg_idx:04d}"
                        segments.append(NovelSegment(
                            segment_id=seg_id,
                            novel_title=novel_stem,
                            chapter=chapter_name,
                            text=current_segment.strip(),
                            position=seg_idx,
                        ))
                        seg_idx += 1
                    current_segment = ""

            # Flush remaining
            if current_segment.strip() and len(current_segment.strip()) >= self.min_segment_chars:
                seg_id = f"novel_{novel_stem}_seg_{seg_idx:04d}"
                segments.append(NovelSegment(
                    segment_id=seg_id,
                    novel_title=novel_stem,
                    chapter=chapter_name,
                    text=current_segment.strip(),
                    position=seg_idx,
                ))
                seg_idx += 1

        # Assign timestamps based on position (simulate passage of time)
        total = len(segments)
        for i, seg in enumerate(segments):
            # Map position to a date range (e.g., 2020-01 to 2021-12)
            progress = i / max(total - 1, 1)
            month = int(progress * 23) + 1
            year = 2020 + (month - 1) // 12
            month = ((month - 1) % 12) + 1
            seg.timestamp_hint = f"{year}-{month:02d}-15"

        self._novels[filename] = segments
        logger.info(
            f"Loaded '{novel_stem}': {total} segments from "
            f"{len(chapters)} chapters"
        )
        return segments

    def _split_chapters(self, text: str, filename: str) -> List[Tuple[str, str]]:
        """Split novel text into chapters."""
        # Try Chinese chapter pattern first
        matches = list(self.CHAPTER_PATTERN.finditer(text))

        if len(matches) >= 2:
            chapters = []
            for i, match in enumerate(matches):
                start = match.start()
                end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
                chapter_name = match.group(0).strip()[:30]
                chapter_text = text[start:end]
                chapters.append((chapter_name, chapter_text))
            return chapters

        # Fallback: split by large blank lines or fixed chunk size
        blocks = re.split(r"\n{2,}", text)
        if len(blocks) <= 5:
            # Very short text, return as single chapter
            return [(Path(filename).stem, text)]

        # Group into ~2000-char chapters
        chapters = []
        current = ""
        chapter_idx = 1
        for block in blocks:
            current += block + "\n\n"
            if len(current) >= 2000:
                chapters.append((f"Part_{chapter_idx}", current))
                current = ""
                chapter_idx += 1
        if current.strip():
            chapters.append((f"Part_{chapter_idx}", current))

        return chapters

    def _split_long_text(self, text: str) -> List[str]:
        """Split long text at sentence boundaries."""
        # Try Chinese sentence boundaries
        sentences = re.split(r"(?<=[。！？；\n])", text)
        segments = []
        current = ""
        for sent in sentences:
            current += sent
            if len(current) >= self.min_segment_chars:
                segments.append(current.strip())
                current = ""
                if len(segments) >= 3:  # Max 3 sub-segments
                    break
        if current.strip() and not segments:
            segments.append(current.strip())
        return segments if segments else [text[:self.max_segment_chars]]

    def get_all_segments(self) -> Dict[str, List[NovelSegment]]:
        """Load all novels and return segments."""
        for book in self.list_books():
            if book not in self._novels:
                try:
                    self.load_novel(book)
                except Exception as e:
                    logger.warning(f"Failed to load {book}: {e}")
        return self._novels

    def get_stats(self) -> dict:
        """Get statistics for all loaded novels."""
        stats = {}
        for name, segments in self._novels.items():
            total_chars = sum(len(s.text) for s in segments)
            stats[name] = {
                "segments": len(segments),
                "total_chars": total_chars,
                "est_tokens": total_chars // 2,
                "avg_segment_chars": total_chars // len(segments) if segments else 0,
            }
        return stats


class NovelQAGenerator:
    """Generate temporal QA pairs from novel content using LLM."""

    def __init__(
        self,
        api_base: str = "https://api.siliconflow.cn/v1",
        api_key: str = "",
        model: str = "deepseek-ai/DeepSeek-V3",
        temperature: float = 0.7,
        max_tokens: int = 4096,
    ):
        self.client = OpenAI(base_url=api_base, api_key=api_key, timeout=120)
        self.model = model
        self.temperature = temperature
        self.max_tokens = max_tokens

    def generate_qa_for_chapter(
        self,
        segments: List[NovelSegment],
        novel_title: str,
        num_questions: int = 10,
    ) -> List[NovelQA]:
        """Generate QA pairs for a set of segments (chapter)."""
        # Build context from segments
        context_parts = []
        for seg in segments:
            context_parts.append(f"[{seg.segment_id}] ({seg.timestamp_hint})\n{seg.text}")
        context = "\n---\n".join(context_parts)

        prompt = f"""你是一个专业的阅读理解测试题生成专家。请根据以下小说片段生成{num_questions}道高质量的多选题。

## 小说信息
书名: {novel_title}
片段数: {len(segments)}

## 小说片段内容

{context}

## 要求
1. 生成{num_questions}道多选题，每题4个选项(A/B/C/D)
2. 题目应覆盖以下类型:
   - 时间推理题(30%): 需要根据时间顺序推理的事件
   - 细节回忆题(30%): 需要从大量文本中找到具体细节
   - 因果推理题(20%): 需要理解事件间的因果关系
   - 综合理解题(20%): 需要综合多个片段的信息
3. 时间推理题应涉及不同时间点的事件对比
4. 难度分配: 简单30%, 中等50%, 困难20%
5. 选项应有合理的干扰项(不能太明显)
6. 答案必须能在原文中找到依据

## 输出格式
严格按以下JSON格式输出，不要输出其他内容:
```json
[
  {{
    "question": "问题内容",
    "choices": ["选项A", "选项B", "选项C", "选项D"],
    "correct_choice_index": 0,
    "answer": "正确答案的完整文本",
    "question_type": "temporal/factual/causal/comprehensive",
    "difficulty": "level_1/level_2/level_3",
    "source_segment_id": "novel_xxx_seg_xxxx"
  }}
]
```"""

        raw = self._call_llm(prompt)
        return self._parse_qa_response(raw, novel_title)

    def _call_llm(self, prompt: str) -> str:
        """Call LLM API with retry."""
        for attempt in range(3):
            try:
                response = self.client.chat.completions.create(
                    model=self.model,
                    messages=[
                        {"role": "system", "content": "你是一个专业的测试题生成专家。只输出JSON格式数据。"},
                        {"role": "user", "content": prompt},
                    ],
                    temperature=self.temperature,
                    max_tokens=self.max_tokens,
                )
                return response.choices[0].message.content.strip()
            except Exception as e:
                logger.warning(f"LLM call failed (attempt {attempt + 1}/3): {e}")
                if attempt < 2:
                    time.sleep(5 * (attempt + 1))
                else:
                    raise

    def _parse_qa_response(self, raw: str, novel_title: str) -> List[NovelQA]:
        """Parse LLM response into NovelQA objects."""
        # Extract JSON
        if "```json" in raw:
            raw = raw.split("```json", 1)[1].split("```", 1)[0].strip()
        elif "```" in raw:
            raw = raw.split("```", 1)[1].split("```", 1)[0].strip()

        try:
            items = json.loads(raw)
        except json.JSONDecodeError as e:
            logger.error(f"Failed to parse QA JSON: {e}")
            return []

        qas = []
        for i, item in enumerate(items):
            try:
                qa = NovelQA(
                    qa_id=f"novel_{novel_title}_qa_{i:04d}",
                    novel_title=novel_title,
                    question=item["question"],
                    gold_answer=item.get("answer", item.get("correct_answer", "")),
                    choices=item.get("choices", []),
                    correct_choice_index=item.get("correct_choice_index"),
                    answer_type="multi_choice" if item.get("choices") else "abstractive",
                    question_type=item.get("question_type", "factual"),
                    source_segment_ids=(
                        [item["source_segment_id"]]
                        if item.get("source_segment_id")
                        else []
                    ),
                    difficulty=item.get("difficulty", "level_2"),
                )
                qas.append(qa)
            except (KeyError, TypeError) as e:
                logger.warning(f"Failed to parse QA item {i}: {e}")

        return qas
